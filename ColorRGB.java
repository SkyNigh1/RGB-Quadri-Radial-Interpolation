public class ColorRGB{
    public int rouge;
    public int vert;
    public int bleu;
    public ColorRGB(int rouge, int vert, int bleu){
        this.rouge=rouge;
        this.vert=vert;
        this.bleu=bleu;
    }

    public int getRouge(){
        return rouge;
    }

    public int getVert(){
        return vert;
    }

    public int getBleu(){
        return bleu;
    }
}